    public void adder ( Shape s , String ss)
    {
                
    mypanel1.addShape(s);
    mypanel1.refresh(null);
    if(ss.equals("circle")){
        jComboBox1.addItem("Oval_"+ccounter);
       ccounter++;
    }
    if(ss.equals("Rectangle")){
        jComboBox1.addItem("Rectangle_ "+rcounter);
    rcounter++;
    }
    if(ss.equals("line")){
        jComboBox1.addItem("LineSegment_"+lcounter);
    lcounter++;
    }
   
    if(ss.equals("Triangle")){
        jComboBox1.addItem("Triangle_"+tcounter);
    tcounter++;
    }
    }
    
    
