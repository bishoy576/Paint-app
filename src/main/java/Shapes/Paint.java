    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
                JFileChooser filechooser = new JFileChooser();
       
        int response = filechooser.showSaveDialog(this);

        if(response == JFileChooser.APPROVE_OPTION)
        {
            
            String path=filechooser.getSelectedFile().getAbsolutePath();
            JsonArray ja=new JsonArray();
            for(Shape shape: mypanel1.getter())
            {
                ja.add(shape.toJsonObject());
            }
            String jsontext = Jsoner.serialize(ja);
            OutputStream os = null;
        try {
            os = new FileOutputStream(new File(path));
            os.write(jsontext.getBytes(), 0, jsontext.length());
            os.close();
            System.out.println("File saved");
        } catch (IOException e) {
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here:
        JFileChooser filechooser = new JFileChooser();
        int response = filechooser.showOpenDialog(this);
        if(response == JFileChooser.APPROVE_OPTION)
        {
            lcounter =1 ;
            rcounter =1 ;
            ccounter =1 ;
            tcounter=1;
            
            JsonArray ja = null;
           mypanel1.getter().clear();
           jComboBox1.removeAllItems();
            jComboBox1.addItem("Choose shape");
            String path = filechooser.getSelectedFile().getAbsolutePath();
            File file = new File(path);
            FileInputStream fileInputStream = null;
            byte[] bFile = new byte[(int) file.length()];
            try {
                fileInputStream = new FileInputStream(file);
                fileInputStream.read(bFile);
                fileInputStream.close();
            
            String jsontext = new String(bFile);
            
                try {
                    ja = (JsonArray) Jsoner.deserialize(jsontext); 
                } catch (JsonException ex) {
                    Logger.getLogger(Paint.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Paint.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Paint.class.getName()).log(Level.SEVERE, null, ex);
            }
           // System.out.println(ja.size());
            for(Object object : ja)
            {
           
                JsonObject jo=(JsonObject) object;
                String type = (String)jo.get("Type");
                if(type.contains("Triangle"))
                {
                    System.out.println("in t if");
                    Shape t = new Triangle(null, null, null);
                    t.fromJsonObject(jo);
                    adder(t,"Triangle");
                    
                }
                if(type.contains("Rectangle"))
                {
                    System.out.println("in r if");
                    Shape r=new Rectangle(0, 0, null);
                    r.fromJsonObject(jo);
                    adder(r,"Rectangle");
                }
                if(type.contains("Oval"))
                {
                    System.out.println("in o if");
                    Shape o=new Circle(null, 0,0);
                    o.fromJsonObject(jo);
                    adder(o,"circle");
                }
                if(type.contains("Line"))
                {
                    System.out.println("in l if");
                    Shape l=new LineSegment(null, null);
                    l.fromJsonObject(jo);
                    adder(l,"line");
                }
            }
            
            
           
            
            
        }
        mypanel1.refresh(null);
    }//GEN-LAST:event_jMenuItem2ActionPerformed

       

    
